package net.helpscout.api.model.customer;

import java.util.Date;
import java.util.List;

/**
 * SearchCustomer -
 *
 * @author briandame@gmail.com
 */
public class SearchCustomer {

    private Long id;
    private String firstName;
    private String lastName;
    private String fullName;
    private List<String> emails;
    private String photoUrl;
    private String photoType;
    private String gender;
    private String age;
    private String organization;
    private String jobTitle;
    private String location;
    private Date createdAt;
    private Date modifiedAt;

    public SearchCustomer() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public List<String> getEmails() {
        return emails;
    }

    public void setEmails(List<String> emails) {
        this.emails = emails;
    }

    public String getPhotoUrl() {
        return photoUrl;
    }

    public void setPhotoUrl(String photoUrl) {
        this.photoUrl = photoUrl;
    }

    public String getPhotoType() {
        return photoType;
    }

    public void setPhotoType(String photoType) {
        this.photoType = photoType;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getOrganization() {
        return organization;
    }

    public void setOrganization(String organization) {
        this.organization = organization;
    }

    public String getJobTitle() {
        return jobTitle;
    }

    public void setJobTitle(String jobTitle) {
        this.jobTitle = jobTitle;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }

    public Date getModifiedAt() {
        return modifiedAt;
    }

    public void setModifiedAt(Date modifiedAt) {
        this.modifiedAt = modifiedAt;
    }

    @Override
    public String toString() {
        return "SearchCustomer [id=" + id + ", firstName=" + firstName + ", lastName=" + lastName + ", fullName="
                + fullName + ", emails=" + emails + ", photoUrl=" + photoUrl + ", photoType=" + photoType + ", gender="
                + gender + ", age=" + age + ", organization=" + organization + ", jobTitle=" + jobTitle + ", location="
                + location + ", createdAt=" + createdAt + ", modifiedAt=" + modifiedAt + "]";
    }

}
