package net.helpscout.api.model.ref;

public class MailboxRef {
	private Long id;
	private String name;

	public Long getId() {
		return id;
	}
	public String getName() {
		return name;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "MailboxRef [id=" + id + ", name=" + name + "]";
	}

}